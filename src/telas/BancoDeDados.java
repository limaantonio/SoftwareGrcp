    private void btnBdExluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBdExluirActionPerformed
        int confirma = JOptionPane.showConfirmDialog(null, 
                        "Tem certeza que deseja excluir esse registro?",
                        "Atenção",
                        JOptionPane.YES_NO_OPTION);
        
        
        if(confirma == JOptionPane.YES_OPTION){
            String sql = "DELETE FROM tbGuias WHERE GUI_ID = ?";
            try{
                pst = conn.prepareStatement(sql);
                pst.setString(1,  tbBanco.getModel().getValueAt(tbBanco.getSelectedRow(), 0).toString());
                int apagado = pst.executeUpdate();
                
                if(apagado > 0){
                    JOptionPane.showMessageDialog(null, "Registro exluído.");
                    selectAll();
                   // tbBanco.setModel(DbUtils.resultSetToTableModel(selectAll()));
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnBdExluirActionPerformed

    public void setarC(){
        int setar =  tbBanco.getSelectedRow();
        Double v1, v2, v3;
        v1 = Double.parseDouble(tbBanco.getModel().getValueAt(setar, 6).toString());
        v2 = Double.parseDouble(tbBanco.getModel().getValueAt(setar, 5).toString());
        v3 = Double.parseDouble(tbBanco.getModel().getValueAt(setar, 4).toString());
       
        txtId.setText(tbBanco.getModel().getValueAt(setar, 0).toString());
        txtNumero.setText(tbBanco.getModel().getValueAt(setar, 1).toString());
        cbSecretaria.setSelectedItem(tbBanco.getModel().getValueAt(setar, 2).toString());
    
        txtBaseCalculo.setText(tbBanco.getModel().getValueAt(setar, 3).toString());
        //txtVencimento.setText(tbGuias.getModel().getValueAt(setar, 4).toString());
        txtValServidor.setText(tbBanco.getModel().getValueAt(setar, 4).toString());
        txtValPatronal.setText(tbBanco.getModel().getValueAt(setar, 5).toString());
        txtValSuplementar.setText(tbBanco.getModel().getValueAt(setar, 6).toString());
        txtObservacao.setText(tbBanco.getModel().getValueAt(setar, 7).toString());
        
        
        SimpleDateFormat sdf = new  SimpleDateFormat("dd/MM/yyyy");
        String data = sdf.format(Date.valueOf(tbBanco.getModel().getValueAt(setar, 8).toString()));
        txtData.setText(data.toString());
        v1 = v1 + v2;
        v3 = v3 + v1;
        
        txtTotalPatronal.setText(v1.toString());
        txtTotalGeral.setText(v3.toString());
        //nao permitir usuario cadastrar novamente o mesmo item
        btnSalvar.setEnabled(false);
        
    }
