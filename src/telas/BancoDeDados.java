    public void pesquisar(){  
        String sql = "SELECT *FROM tbGuias WHERE MONTH(GUI_DATA_CADASTRO) = ? "
                + "AND YEAR(GUI_DATA_CADASTRO) = ?" ;
       
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtPesquisar.getText().substring(1, 3));
            pst.setString(2, txtPesquisar.getText().substring(3, 7));
            rs = pst.executeQuery();
            
            tbBanco.setModel(DbUtils.resultSetToTableModel(rs));
             
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
              
        int setar = tbBanco.getSelectedRow();
    }
    
